// מפתח ייחודי לפריט תפריט (קטגוריה|תת-קטגוריה|פריט) לצורך סימון/שמירה
export const MENU_KEY_SEP = '|||';

export const menuItemKey = (category: string, subName: string, itemName: string): string =>
  [category, subName, itemName].join(MENU_KEY_SEP);

export interface ParsedMenuKey {
  category: string;
  subCategory: string;
  item: string;
}

export const parseMenuKey = (key: string): ParsedMenuKey => {
  const [category = '', subCategory = '', item = ''] = key.split(MENU_KEY_SEP);
  return { category, subCategory, item };
};
